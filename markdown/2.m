Геометрично це можна зобразити так: намалюйте квадрат ([Зображення
1](#figure1)), сторона якого буде представляти $x$. Тепер припустімо, що
квадрат зростає, додаючи до його розміру частинку $dx$ в обох напрямках.
Збільшений квадрат складається з початкового квадрата з площею $x^2$,
двох прямокутників угорі та праворуч, кожен з яких має площу $x · dx$
(або разом $2x · dx$), і маленький квадрат у верхньому правому куті,
який дорівнює $(dx)^2$. На [Зображенні 2](#figure2) ми зобразили $dx$ як
досить велику частину від $x$ -- приблизно $\frac{1}{5}$. Але
припустімо, що ми взяли його лише $\frac{1}{100}$ -- приблизно товщиною
лінії, намальованої тонким пером. Тоді маленький кутовий квадрат матиме
площу лише $\frac{1}{10 000}$ від $x^2$ і буде практично невидимим.
Тобто, $(dx)^2$ буде незначний, тільки якщо ми вважаємо, що приріст $dx$
сам по собі досить малий.

