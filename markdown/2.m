Розглянемо інше порівняння.

Припустімо, що мільйонер сказав своєму секретарю: наступного тижня я
віддам тобі невелику частину будь-яких грошей, які мені надійдуть.
Припустімо, що секретар сказав своєму синові: «Я віддам тобі невелику
частину того, що я отримаю». Припустімо, що частина у кожному випадку
дорівнює $\frac{1}{100}$. Так, якщо містер Мільйонер отримав протягом
наступного тижня £$1000$, секретар отримає £$10$, а хлопчик -- $2$
шилінги. Десять фунтів були б невеликою величиною порівняно з £$1000$;
але два шилінги --- це двічи мала величина, другорядного порядку. Але
якою була б диспропорція, якби частина замість $\frac{1}{100}$ була
визначена як $\frac{1}{1000}$? Тоді, у той час, як містер Мільйонер
отримав свої £$1000$, пан секретар отримав би лише £$1$, а хлопчик менше
одного фартингу!

Дотепний Дін Свіфт\* якось написав:

>     «Отже, зауважують натуралісти, у блохи 
>     є менші блохи, які полюють на неї.
>     А в них є менші блохи, що їх кусають,
>     і так до нескінченності». 

*\*On Poetry: a Rhapsody* (сторінка 20), надрукована 1733 р. -- зазвичай
неправильно цитується.

Вола може непокоїти блоха звичайного розміру -- маленька істота першого
порядку малості. Але він, мабуть, не буде турбуватися про блошину блоху;
будучи другого порядку малості, вона була б незначною. Навіть купа
блошиних бліх не мала б значення для вола.\

