#  Про різні ступені малості

Ми виявимо, що в наших процесах обчислення ми маємо справу з малими
величинами різного ступеня малості.

Нам також доведеться дізнатися, за яких обставин ми можемо вважати малі
величини настільки незначними, що ми можемо їх не враховувати. Усе
залежить від відносної малості.

Перш ніж зафіксуємо будь-які правила, згадаймо деякі схожі випадки. У
годині $60$ хвилин, у добі $24$ години, у тижні $7$ днів. Таким чином, є
$1440$ хвилин на день і $10080$ хвилин на тиждень.

Вочевидь, $1$ хвилина - це дуже мала кількість часу в порівнянні з цілим
тижнем. Дійсно, наші предки вважали її малою порівняно з годиною і
називали «одна хвилина» (англ. «minute» - незначний), що означає малу
частину години, а саме одну шістдесяту. Коли вони вимагали ще менших
відрізків часу, вони ділили кожну хвилину на ще менші частини по $60$,
які за часів королеви Єлизавети називали «другими хвилинами» (*тобто:*
незначні величини другого порядку малості). Зараз ці малі величини
другого порядку малості ми називаємо «секунди» (англ. «second» -
другий). Але мало хто знає, *чому* вони так називаються.

Якщо одна хвилина така мала в порівнянні з цілим днем, то наскільки ж
меншою у порівнянні є одна секунда!

Або подумайте про фартинг ($\frac{1}{960}$ фунту стерлінгів) у
порівнянні з совереном ($1$ фунт стерлінгів): він коштує ледве більше,
ніж $\frac{1}{1000}$ соверену. Фартинг має дуже мале значення порівняно
з совереном: його, безперечно, можна розглядати як *малу* величину.
Тепер порівняйте фартинг із £1000 (фунти стерлінгів): відносно цієї
більшої суми фартинг не більш важливий, ніж $\frac{1}{1000}$ фартинга
для соверена. При цьому, навіть золотий соверен є відносно незначною
величиною в багатстві мільйонера.

Якщо ми зафіксуємо будь-яку чисельну частину як таку, що для якихось
цілей є відносно малою, ми легко можемо визначити інші частини ще
більшого ступеня малості. Таким чином, якщо з погляду часу
$\frac{1}{60}$ назвати *малою* частиною, тоді $\frac{1}{60}$ від
$\frac{1}{60}$ (*мала* частинка *малої* частини) можна розглядати як
*невелику величину другого порядку*[]{#smallness} малості.\*

\*Математики говорять про другий порядок «величини», коли вони насправді
мають на увазі другий порядок *малості*. Це дуже бентежить початківців.

Або, якби для будь-якої мети ми взяли $1$ відсоток (*тобто*
$\frac{1}{100}$) як *малу* частину, тоді $1$ відсоток від $1$ відсотка
(*тобто* $\frac{1}{10 000}$) буде малою частиною другого порядку
малості; і $\frac{1}{1 000 000}$ буде невеликою частиною третього
порядку малості, що становить $1$ відсоток від $1$ відсотка від $1$
відсотка. []{#fn1-top}

Нарешті, припустимо, що з якоюсь дуже точною метою ми повинні вважати
$\frac{1}{1,000,000}$ «малим». Таким чином, щоб першокласний хронометр
не відставав і не поспішав більше ніж на пів хвилини на рік, він повинен
рахувати час з точністю до $1$ частинки від $1 051 200$. Тож, якщо
для цієї мети ми розглядаємо $\frac{1}{1,000,000}$ (або одну мільйонну)
як невелику кількість, тоді $\frac{1}{1,000,000}$ від
$\frac{1}{1,000,000 }$, тобто $\frac{1}{1,000,000,000,000}$ (або одна
трильйонна^[†](#fn1)^) буде малою величиною другого порядку малості, і
нею можна повністю знехтувати для порівняння.

Тепер ми бачимо, що чим більш незначною є сама мала величина, тим ще
більш незначною стає відповідна мала величина другого порядку. Тому ми
знаємо, що *у всіх випадках ми маємо право нехтувати малими величинами
другого -- третього (або вищого) порядку*, тільки якщо ми зробимо малу
величину першого порядку досить малою саму по собі.

Але слід пам'ятати, що малі величини, якщо зустрічаються в наших виразах
помноженими на якийсь інший множник, можуть стати важливими, якщо цей
множник сам по собі великий. Навіть фартинг стає важливим, якщо його
помножити на кілька сотень.

Так, у математичному аналізі ми пишемо $dx$, маючи на увазі малу
частинку від $x$. Такі речі, як $dx$, $du$ і $dy$, називаються
«диференціалами», тобто диференціалами $x$, або $u$, або $y$, залежно
від випадку. \[*Читається* як *де-ікс*, або *де-ю*, або *де-ігрек*.\]
Якщо $dx$ є малою частинкою $x$, і відносно невеликий сам по собі, це не
означає, що такими величинами як $x · dx$, або $x^2\, dx$, або
$a^x\, dx$ можна знехтувати як малими. Але $dx × dx$ було б незначним,
будучи малою величиною другого порядку.

Проілюструємо на дуже простому прикладі.

Подумаймо про $x$ як про величину, яка може трохи зрости, щоб стати
$x + dx$, де $dx$ --- невеликий приріст, доданий зростанням. Квадрат
цієї величини дорівнює $x^2 + 2x · dx + (dx)^2$. Другим доданком не
можна нехтувати, оскільки це величина першого порядку; тоді як третій
член має другий порядок малості, будучи частинкою частинки від $x^2$.
Таким чином, якщо ми приймемо $dx$ як чисельне значення, скажімо,
$\frac{1}{60}$ від $x$, тоді другий член буде $\frac{2}{60}$ від $x^2$,
у той час як третій член буде $\frac{1}{3600}$ від $x^2$. Цей останній
член виразу явно менш важливий, ніж другий. Але якщо ми підемо далі й
приймемо $dx$ лише як $\frac{1}{1000}$ від $x$, тоді другий член буде
$\frac{2}{1000}$ від $x^2$, а третій член становитиме лише
$\frac{1}{1 000 000}$ від $x^2$.

