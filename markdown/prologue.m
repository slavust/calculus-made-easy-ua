#  Пролог

Зважаючи на те, скільки дурнів розуміють математичний аналіз, дивно, що
для будь-якого іншого дурня опанувати ті самі засоби вважається або
складним, або нудним завданням.

Деякі хитрощі математичного аналізу досить прості. Деякі надзвичайно
складні. Дурні, які пишуть підручники з вищої математики --- а вони
здебільшого розумні дурні --- рідко намагаються показати вам, наскільки
насправді розрахунки легкі. Навпаки, вони, здається, хочуть вразити вас
своєю надзвичайною кмітливістю, зробивши це найскладнішим шляхом.

Бувши надзвичайно дурним хлопцем, мені довелося звільнитися від
труднощів, і зараз я представляю моїм товаришам-дурням частини, які не є
складними. Опануйте їх ретельно, і решта прийде. Те, що може зробити
один дурень, може й інший.

