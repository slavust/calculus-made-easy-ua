[]{#differ} Тепер, якщо ми поглянемо назад [сюди](4.md#diffexample), ми
побачимо, що просте диференціювання $x^2$ дає нам $2x$. Отже, похідна
від $7x^2$ просто в $7$ разів більше, ніж від $x^2$. Якби ми взяли
$8x^2$, похідна вийшла б у вісім разів більшою, ніж від $x^2$. Якщо
взяти $y = ax^2$, ми отримаємо $$ \frac{dy}{dx} = a × 2x. $$

Якби ми почали з $y = ax^n$, ми мали б отримати
$\dfrac{dy}{dx} = a×nx^{n-1}$. Таким чином, будь-яке просте множення на
константу залишається простим множенням, коли вираз диференціюється. І
те, що вірно для множення, однаково вірно для *ділення*: якби у
наведеному вище прикладі ми взяли константу $\frac{1}{7}$ замість $7$,
ми мали б отримати те саме $\frac{1}{7}$ у результаті після
диференціювання.

*Деякі Додаткові Приклади*. Наступні розгорнуті приклади дозволять вам
повністю опанувати процес диференціювання у застосуванні до звичайних
алгебраїчних виразів і дозволять вам самостійно розв\'язати приклади,
наведені в кінці цього розділу.

\(1\) Продиференціюйте $y = \dfrac{x^5}{7} - \dfrac{3}{5}$.

$\dfrac{3}{5}$ є доданою константою та зникає (див.
[тут](5.md#addconst)).

Тоді ми можемо одразу записати
$$\begin{align*}\frac{dy}{dx} &= \frac{1}{7} × 5 × x^{5-1},
  \\ \text{або}\; \frac{dy}{dx} &= \frac{5}{7} x^4.\end{align*}$$

\(2\) Продиференціюйте $y = a\sqrt{x} - \dfrac{1}{2}\sqrt{a}$.

Терм $\dfrac{1}{2}\sqrt{a}$ зникає, як додана константа; а оскільки
$a\sqrt{x}$ ні що інше, як $ax^{\frac{1}{2}}$, ми маємо:
$$\begin{align*}\frac{dy}{dx} &= a
  × \frac {1}{2} × x^{\frac{1}{2}-1} = \frac{a}{2} × x^{-\frac{1}{2}}, \\ \text{
  або}\; \frac{dy}{dx} &= \frac{a}{2\sqrt{x}}.\end{align*}$$

\(3\) Якщо $ay + bx = by - ax + (x+y)\sqrt{a^2 - b^2}$, знайдіть похідну
$y$ відносно $x$.

Як правило, вираз такого роду потребує трохи більше знань, ніж ми набули
досі; однак завжди варто перевірити, чи можна подати вираз у простішій
формі.

Спочатку ми повинні спробувати привести його до форми $y = {}$ будь-який
вираз, що містить лише $x$.

Вираз можна записати як
$$ (a-b)y + (a + b)x = (x+y) \sqrt{a^2 - b^2}. $$

