#  Наступний етап. Що робити з константами

У наших рівняннях ми розглядаємо $x$ як такий, що зростає, і в
результаті того, що $x$ зростає, $y$ також змінює своє значення та
зростає. Зазвичай ми думаємо про $x$ як про величину, яку ми можемо
змінювати; і розглядаючи зміну $x$ як свого роду *причину*, ми
розглядаємо зміну $y$ як *результат*. Іншими словами, ми вважаємо
значення $y$ залежним від значення $x$. І $x$, і $y$ є змінними, але $x$
--- це те, чим ми оперуємо, а $y$ --- це «залежна змінна». У всьому
попередньому розділі ми намагалися знайти правила для пропорції варіації
в залежній $y$ до варіації в незалежній $x$.

[]{#diffrule2} Наш наступний крок - з\'ясувати, який вплив на процес
диференціювання має наявність *констант*, тобто чисел, які не
змінюються, коли $x$ або $y$ змінюють свої значення.

*Додавання константи.*

[]{#addconst} Почнемо з простого випадку доданої константи, таким чином:
$$\begin{align*} \text{нехай} \; y=x^3+5. \end{align*}$$ Як і раніше, припустімо, що $x$ зростає до $x+dx$, а $y$ зростає до $y+dy$.
$$\begin{align*} \text{Тоді:}\; y + dy
  &= (x + dx)^3 + 5 \\ &= x^3 + 3x^2\, dx +\\&+ 3x(dx)^2 + (dx)^3 + 5.
  \end{align*}$$ Якщо знехтувати малими величинами вищих порядків, це
стане $$\begin{align*} y + dy &= x^3 + 3x^2·dx + 5. \\ \end{align*}$$
Віднімаємо початкове $y = x^3 + 5$ і залишаємо:
$$\begin{align*} dy &= 3x^2\, dx. \\
  \frac{dy}{dx} &= 3x^2. \end{align*}$$

Тож число $5$ повністю зникло. Воно нічого не додало до зростання $x$ і
не входить у похідну. Якби ми поставили $7$, або $700$, або будь-яке
інше число замість $5$, воно б теж зникло. Отже, якщо ми візьмемо літеру
$a$, або $b$, або $c$ для представлення будь-якої константи, вона просто
зникне, коли ми диференціюємо.

Якби додаткова константа мала негативне значення, наприклад $-5$ або
$-b$, вона б так само зникла.

*Помноження на константу.*

Як простий експеримент, розглянемо наступний випадок:

Нехай $y = 7x^2$. Тоді, продовжуючи як і раніше, ми отримуємо:
$$\begin{align*} y + dy &= 7(x+dx)^2 \\ &= 7\{x^2 + 2x·dx + (dx)^2\}
  \\ &= 7x^2 + 14x·dx + 7(dx)^2. \\ \end{align*}$$ Далі, віднімаючи
початкове $y = 7x^2$ та нехтуючи останнім членом, ми маємо
$$\begin{align*} dy
  &= 14x·dx.\\ \frac{dy}{dx} &= 14x. \end{align*}$$

Проілюструємо цей приклад, побудувавши графіки рівнянь $y = 7x^2$ та
$\dfrac{dy}{dx} = 14x$, присвоївши $x$ набір послідовних значень $0$,
$1$, $2$, $3$ тощо, а також знайдемо відповідні значення $y$ та
$\dfrac{dy}{dx}$.

Ці значення ми зводимо в таблицю наступним чином:

  ------------------ -------- --------- --------- --------- ---------- ---------- --------- ---------- -----------
  $x$                $0$      $1$       $2$       $3$       $4$        $5$        $-1$      $-2$       $-3$
  $y$                $0$      $7$       $28$      $63$      $112$        $175$      $7$       $28$       $63$
  $\dfrac{dy}{dx}$   $0$      $14$      $28$      $42$      $56$        $70$       $-14$     $-28$      $-42$
  ------------------ -------- --------- --------- --------- ---------- ---------- --------- ---------- -----------

