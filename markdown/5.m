\(4\) Об'єм циліндра з радіусом $r$ і висотою $h$ визначається формулою
$V = \pi
  r^2 h$. Знайдіть швидкість варіювання об'єму залежно від радіуса, коли
$r =
  5.5$ дюйма та $h=20$ дюймів. Якщо $r = h$, знайдіть такі розміри
циліндра, щоб зміна $1$ дюйм у радіусі викликала зміну у $400$ куб.
дюймів в об\'ємі.

Швидкість зміни $V$ відносно $r$ дорівнює
$$ \frac{dV}{dr} = 2 \pi r h. $$

Якщо $r = 5.5$ дюйми та $h=20$ дюймів. це становить $690.8$. Це
означає, що зміна радіуса на $1$ дюйм призведе до зміни об\'єму на
$690.8$ куб. дюймів. Це легко перевірити, оскільки обсяги з $r = 5$ і
$r = 6$ складають $1570$ куб. дюймів і $2260.8$ куб. дюймів відповідно,
і $2260.8 - 1570 = 690.8$.

Крім того, якщо $$\begin{align*}r=h,\quad \dfrac{dV}{dr} = 2\pi r^2 = 400\quad \\
  \text{та}\quad r = h = \sqrt{\dfrac{400}{2\pi}} = 7.98 \text{ дюймів}.\end{align*} $$

\(5\) Показання $\theta$ пірометра Фері пов'язане з температурою $t$ за
Цельсієм спостережуваного тіла співвідношенням
$$ \dfrac{\theta}{\theta_1} =
  \left(\dfrac{t }{t_1}\right)^4, $$ де $\theta_1$ --- це показання, що
відповідає відомій температурі $t_1$ спостережуваного тіла.

Порівняйте чутливість пірометра при температурах $800°$C., $1000°$C.,
$1200°$C., враховуючи, що він показав $25$, коли температура була
$1000°$C.

Чутливість -- це швидкість зміни показників зі зміною температури, тобто
$\dfrac{d\theta}{dt}$. Формулу можна записати як $$ \theta =
  \dfrac{\theta_1}{t_1^4} t^4 = \dfrac{25t^4}{1000^4}, $$ і ми маємо $$
  \dfrac{d\theta}{dt} = \dfrac{100t^3}{1000^4} = \dfrac{t^3}{10 000 000 000}. $$

Коли $t=800$, $1000$ і $1200$, ми отримуємо
$\dfrac{d\theta}{dt} = 0.0512$, $0.1$ і $0.1728$ відповідно.

Чутливість приблизно подвоюється від $800°$ до $1000°$, і стає на три
чверті більшою при $1200°$.

